#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    long long num1, num2;
    cin >> num1;
    cin >> num2;
    cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;
    cout << num1 << " * " << num2 << " = " << num1 * num2 << endl;
    cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;
    return 0;
}